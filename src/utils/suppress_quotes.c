#include "../../include/minishell.h"

void	take_away_quotes(char *s)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] == '\'' || s[i] == '\"')
		{
			i++;
		}
		else
		{
			s[j] = s[i];
			i++;
			j++;
		}
	}
	s[j] = '\0';
}
